const meubleMap = new Map (meubles);

export function randomMeuble() {
  return meubles[Math.floor(Math.random() * meubles.length)][0];
}

export function getMeuble(name) {
  const meu = meubleMap.get(name);
  if (! meu) {
    console.log(`Warning: ${name} is not a known meuble`);
  }
  return meu ? meu.path : '';
}

export function getMeubles() {
  return meubles;
}
